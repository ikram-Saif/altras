            <h3>How Anti Money Laundering Works</h3>
            <p>AML laws and regulations target criminal activities including market manipulation, trade in illegal goods, corruption of public funds and tax evasion, as well as the methods used to conceal these crimes and the money derived from them.</p>
            <p>Criminals often "launder" money they obtain through illegal acts such as drug trafficking so the funds cannot be easily traced to them. One common technique is to run the money through a legitimate cash-based business owned by the criminal organization or its confederates. The supposedly legitimate business deposits the money, which the criminals can then withdraw.</p>
            <p>Money launderers may also sneak cash into foreign countries to deposit, deposit cash in smaller increments to avoid arousing suspicion, or use illicit cash to buy other cash instruments. Launderers will sometimes invest the money, using dishonest brokers willing to ignore the rules in return for large commissions.</p>
            <p>One rule in place is the AML holding period, which requires deposits to remain in an account for a minimum of five trading days. This holding period is intended to help in anti-money laundering and risk management.</p>
            <h3>Reporting Suspicious Activity</h3>
            <p>It's up to financial institutions to monitor customer deposits and other transactions to ensure they aren't part of a money-laundering scheme. The institutions must verify the origin of large sums, monitor suspicious activities, and report cash transactions exceeding $10,000.2﻿ 3﻿ In addition to complying with AML laws, financial institutions must ensure clients are aware of them.</p>
            <p>Money-laundering investigations by law enforcement often involve scrutinizing financial records for inconsistencies or suspicious activity. In today's regulatory environment, extensive records are kept on just about every significant financial transaction. So when police attempt to trace a crime to its perpetrators, few methods are more effective than examining the records of connected financial transactions.</p>
            <p>In cases of robbery, embezzlement or larceny, law enforcement are often able to return the funds or property uncovered during a money-laundering investigation to the victims of the crime. For example, if an agency discovers money a criminal laundered to cover up embezzlement, the agency can usually trace it back to those from whom the funds were embezzled.</p>
            <h3>AML vs. KYC</h3>
            <p>While closely related, there is a difference between AML and know your client (KYC). In banking, KYC rules are the steps institutions must take to verify their customers' identities. AML operates on a broader level: they are the measures institutions take to prevent and combat money laundering, terrorism financing and other financial crimes. Banks use AML and KYC compliance to maintain secure financial institutions.</p>
            <h3>History of Anti Money Laundering</h3>
            <p>Anti-money laundering initiatives rose to global prominence in 1989, when a group of countries and organizations around the world formed the Financial Action Task Force (FATF). Its mission is to devise international standards to prevent money laundering and promote their implementation. In October 2001, following the 9/11 terrorist attacks, FATF expanded its mandate to include combating terrorist financing.</p>
